

public class PracticaBucleIV5Y6 {

    public static void main(String[] args) {

        //(Con bucle do while), imprimir los números del 1-100 
        int num = 1;
        do {
            System.out.println(num);
            num++;

        } while (num <= 100);

        
        //(Con bucle while), imprimir los números del 1-100
        int num1 =1;
                
        while(num1<=100){
            System.out.println(num1);
            num1++;
        }
        
        
        
    }
}
